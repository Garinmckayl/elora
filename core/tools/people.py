def list_people(user_id: str = "") -> dict:
    """Return a summary of everyone Elora knows about.

    Use this when the user asks "who do you know?" or "what do you know about
    my contacts?" or when you need to find someone's contact to send them a message.

    Args:
        user_id: Injected automatically.

    Returns:
        dict: {"people": [...], "count": int}
    """
    from elora_agent.shared import get_user_id
    uid = user_id or get_user_id()

    people = _get_all_people(uid)
    summaries = [
        {
            "name": p.get("name", ""),
            "relationship": p.get("relationship", ""),
            "email": p.get("contact_email", ""),
            "phone": p.get("contact_phone", ""),
            "notes_preview": (p.get("notes", "") or "")[:80],
        }
        for p in people
    ]
    return {"people": summaries, "count": len(summaries)}


# ---------------------------------------------------------------------------
# Tool: update appearance from a photo (called after camera capture)
# ---------------------------------------------------------------------------

def update_person_appearance(name: str, appearance_description: str, user_id: str = "") -> dict:
    """Update the visual appearance description for a known person.

    Call this when the user shows Elora a photo and says "this is [name]"
    or "remember what she looks like". Use Gemini's vision description
    as the appearance_description.

    Args:
        name:                   Name of the person to update.
        appearance_description: Visual description from the photo.
        user_id:                Injected automatically.

    Returns:
        dict: {"status": "ok"} or {"status": "not_found"}
    """
    from elora_agent.shared import get_user_id
    uid = user_id or get_user_id()

    existing = _find_by_name(uid, name)
    if not existing:
        return {"status": "not_found", "name": name,
                "suggestion": f"I don't know {name} yet. Try: remember_person(name='{name}', ...)"}

    col = _people_col(uid)
    updates = {
        "appearance_description": appearance_description,
        "face_embedding_text": _build_face_text(name, existing.get("relationship", ""), appearance_description),
        "updated_at": datetime.now(timezone.utc).isoformat(),
    }

    if col:
        try:
            col.document(existing["id"]).update(updates)
        except Exception as e:
            logger.warning(f"[People] Firestore update failed, using in-memory fallback: {e}")
            uid_store = _mem_store.get(uid, {})
            if existing["id"] in uid_store:
                uid_store[existing["id"]].update(updates)
    else:
        uid_store = _mem_store.get(uid, {})
        if existing["id"] in uid_store:
            uid_store[existing["id"]].update(updates)

    logger.info(f"[People] Updated appearance for '{name}' for user={uid}")
    return {"status": "ok", "name": name}


# ---------------------------------------------------------------------------
# Photo matching helper (used by photo search)
# ---------------------------------------------------------------------------

def find_people_in_description(photo_description: str, user_id: str) -> list[dict]:
    """Given a Gemini vision description of a photo, find known people who match.

    Returns a list of matching person profiles, best-match first.
    """
    people = _get_all_people(user_id)
    matches = []

    desc_lower = photo_description.lower()
    for p in people:
        score = 0
        name_lower = p.get("name", "").lower()
        rel_lower = p.get("relationship", "").lower()
        appearance = p.get("appearance_description", "").lower()

        # Name appears in photo description (e.g. tagged, mentioned)
        if name_lower and name_lower in desc_lower:
            score += 10

        # Appearance features match
        if appearance:
            for feature in appearance.split(","):
                f = feature.strip()
                if f and f in desc_lower:
                    score += 2

        # Relationship keywords
        if rel_lower in desc_lower:
            score += 3

        if score > 0:
            matches.append({"score": score, "person": _format_person(p)})

    matches.sort(key=lambda x: x["score"], reverse=True)
    return [m["person"] for m in matches]
